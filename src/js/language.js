// Language configuration
const languageConfig = {
    pt: { name: 'Portugu√™s', flag: 'üáµüáπ' },
    en: { name: 'English', flag: 'üá¨üáß' },
    es: { name: 'Espa√±ol', flag: 'üá™üá∏' }
};

// Default language
let currentLanguage = 'pt';

// Change language function that works with your CSS system
function changeLanguage(lang) {
    if (translations[lang]) {
        // Remove all language classes from body
        document.body.classList.remove('lang-pt', 'lang-en', 'lang-es', 'lang-he', 'lang-ru', 'lang-it');
        
        // Add new language class
        document.body.classList.add('lang-' + lang);
        
        currentLanguage = lang;
        localStorage.setItem('w-kosher-language', lang);
        
        // Update language selector
        updateLanguageSelector();
        
        // Update active button
        updateActiveButton(lang);
    }
}

// Update language selector dropdown
function updateLanguageSelector() {
    const currentFlag = document.getElementById('current-flag');
    const currentLang = document.getElementById('current-lang');
    
    if (currentFlag && currentLang) {
        currentFlag.textContent = languageConfig[currentLanguage].flag;
        currentLang.textContent = languageConfig[currentLanguage].name;
    }
}

// Update active language button
function updateActiveButton(activeLang) {
    // Remove active class from all buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
    });
    
    // Add active class to current language button
    const activeBtn = document.querySelector(`[onclick="changeLanguage('${activeLang}')"]`);
    if (activeBtn) {
        activeBtn.classList.add('active');
    }
}

// Initialize language system
document.addEventListener('DOMContentLoaded', function() {
    // Load saved language
    const savedLanguage = localStorage.getItem('w-kosher-language');
    if (savedLanguage && translations[savedLanguage]) {
        currentLanguage = savedLanguage;
        changeLanguage(savedLanguage);
    } else {
        // Set default to Portuguese
        changeLanguage('pt');
    }
    
    // Setup dropdown functionality
    const languageSelector = document.querySelector('.language-selector');
    const languageDropdown = document.getElementById('languageDropdown');
    
    if (languageSelector && languageDropdown) {
        languageSelector.addEventListener('click', function(e) {
            e.stopPropagation();
            languageDropdown.style.display = 
                languageDropdown.style.display === 'block' ? 'none' : 'block';
        });
        
        // Close dropdown when clicking outside
        document.addEventListener('click', function() {
            languageDropdown.style.display = 'none';
        });
        
        // Handle dropdown options
        document.querySelectorAll('.language-option').forEach(option => {
            option.addEventListener('click', function() {
                const span = this.querySelector('span');
                if (span) {
                    const languageText = span.textContent;
                    let selectedLang = 'pt';
                    
                    if (languageText === 'English') selectedLang = 'en';
                    else if (languageText === 'Espa√±ol') selectedLang = 'es';
                    else if (languageText === '◊¢◊ë◊®◊ô◊™') selectedLang = 'he';
                    else if (languageText === '–†—É—Å—Å–∫–∏–π') selectedLang = 'ru';
                    else if (languageText === 'Italiano') selectedLang = 'it';
                    
                    changeLanguage(selectedLang);
                    languageDropdown.style.display = 'none';
                }
            });
        });
    }
});

